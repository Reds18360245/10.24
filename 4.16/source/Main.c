#include<stdio.h>
#include<stdlib.h>

int main()
{
	char a = '*';
	int b, c, d;
	int e = 0;
	int f = 0;

	printf("(A)\n");
	for (b = 0; b <= 9; b++)
	{
		for (c = 0; c <= b; c++)
		{
			printf("%c", a);
		}
		printf("\n");
	}
	printf("\n");
	/*----------------------------------------------------------------------------------------------------------------------------*/
	printf("(B)\n");
	for (b = 9; b >= 0; b--)
	{
		for (c = b; c >= 0; c--)
		{
			printf("%c", a);
		}
		printf("\n");
	}
	printf("\n");
	/*----------------------------------------------------------------------------------------------------------------------------*/
	printf("(C)\n");
	for (b = 9; b >= 0; b--)
	{
		f++;
		for (d = 1; d < f; d++)
		{
			printf(" ");
		}
		for (c = b; c >= 0; c--)
		{
			printf("%c", a);
		}
		printf("\n");
	}
	printf("\n");
	/*----------------------------------------------------------------------------------------------------------------------------*/
	printf("(D)\n");
	for (b = 0; b < 10; b++)
	{
		e++;
		for (d = 9; d >= e; d--)
		{
			printf(" ");
		}
		for (c = 0; c <= b; c++)
		{
			printf("%c", a);
		}
		printf("\n");
	}

	system("pause");
	return 0;

}